        function createFieldOverlay(key, value, pos) {
            const overlay = document.createElement('div');
            overlay.className = 'field-overlay';
            overlay.id = 'overlay-' + key;
            overlay.setAttribute('data-key', key);
            
            // Convert mm to pixels for display
